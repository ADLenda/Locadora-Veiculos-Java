    private void botaoPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPagarActionPerformed
        Float pagamento = Float.parseFloat(JOptionPane.showInputDialog("Digite o valor a ser pago:"));
        if((pagamento > TelaInicial.listaCliente.get(info).getDivida())){
            JOptionPane.showMessageDialog(null,"Pagamento maior que a divida!","Erro!",JOptionPane.ERROR_MESSAGE);
        }else if((pagamento < 0)){
            JOptionPane.showMessageDialog(null,"Pagamento indevido!","Erro!",JOptionPane.ERROR_MESSAGE);
        }else{
            TelaInicial.listaCliente.get(info).setDivida(TelaInicial.listaCliente.get(info).getDivida()-pagamento);
            JOptionPane.showMessageDialog(null,"Sua dívida e de R$"+TelaInicial.listaCliente.get(info).getDivida(),"Pagamento Concluído!",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_botaoPagarActionPerformed

