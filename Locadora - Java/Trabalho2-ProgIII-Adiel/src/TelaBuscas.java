    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarActionPerformed
        if(txtFieldINFO.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Insira um dado a ser pesquisado!","Erro!",JOptionPane.ERROR_MESSAGE);
        }else{
            info = TelaInicial.buscaCliente(TelaInicial.listaCliente, Integer.parseInt(txtFieldINFO.getText()));
                if(info != -1){
                    txtAreaINFO.setFont(new Font("Arial",Font.PLAIN,12));
                    txtAreaINFO.append("Nome: "+TelaInicial.listaCliente.get(info).getNome()
                        +"\nDivida: "+TelaInicial.listaCliente.get(info).getDivida()+"\n"
                        );
                }else{
                    JOptionPane.showMessageDialog(null,"Cliente não existe!","Erro!",JOptionPane.ERROR_MESSAGE);
                }
        }
    }//GEN-LAST:event_botaoPesquisarActionPerformed

